              <p id="aboutz" class="rem">GEOFFREY ONWUZULIKE IBEAGHA FOUNDATION –Goif, Parent of Grassroots Outreach Initiative Frontiers is a not-for-profit and non-governmental organization with a primary interest in developing communities by taking action at the basic level in the areas of Spiritual life, health, education, and Social welfare. 
                We are a bridge between benefactors who share in our mission and vision to care for the social welfare (Shelter, food and clothing) education, health  and spiritual life of the less privileged and vulnerable children such as young indigents orphans, adolescents, children and the destitute.
                Goif activities started in 2013, as an informal association of individuals with a common interest of assisting the less privileged, within and around their communities, coming together to source for resources among themselves to collectively care for the education of identified less privileged children in the society like young orphans, adolescents and indigents in general.
                Over the years, the number of beneficiaries continued to grow, making the fund from individual member contributions inadequate to satisfy the objectives.
                About three years on in 2016, the decision was taken as a necessity to formalize the association and seek external assistance hence the incorporation of Goif so as to extend the good work to a larger society.
                At the Goif, we are actualizing our mission supported by a board of trustees, staff and volunteers comprising professionals in Education, Nutrition, Health, Finance, Engineering and Administration with a combined vast experience in social outreach and programs.
                Goif’ activity is currently based in Akwa-Ibom, Abia, Enugu, Ebonyi, Markudi, and Abuja FCT and in Anambra States of Nigeria.
                

